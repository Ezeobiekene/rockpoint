import mongoose from "mongoose";

const Schema = mongoose.Schema

const ladiespostSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    image: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    content: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export const ladiespostModel = mongoose.model("Ladiespost", ladiespostSchema);
